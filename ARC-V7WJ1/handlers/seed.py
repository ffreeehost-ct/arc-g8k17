def seed_countries():
    existing = db.get_all_categories()
    if len(existing) >= 50:
        return
    items = []
    for code, name, price in ALL_COUNTRIES:
        # deduplicate by code
        if not any(c[1] == code for c in existing):
            flag = get_flag(code)
            items.append((code, name, flag, price))
    if items:
        db.bulk_add_categories(items)
        print(f'[SEED] загружено {len(items)} стран')


